            <h3>History</h3>
            <p>
            Boudhanath also called Boudha is a stupa in Kathmandu, Nepal. Boudha stupa is semicircle shaped and contains the relics and remains of Buddha. The stupa’s massive mandala makes it one of the largest spherical stupas in the world.  Boudha Stupa was listed in world heritage list by UNESCO in 1979 and it is one of the most popular tourist sites in Kathmandu. The stupa dates from 600AD and was built by a Tibetan king. The stupa is a historical pilgrimage site for all the Buddhists around the world. The stupa was built using many kilograms of gold for decoration.       </p>
          

